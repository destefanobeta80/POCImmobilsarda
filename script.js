function changeTab(evt, tabName) {
    // Ottiene tutti i tab link e i contenuti dei tab
    let tabLinks = document.getElementsByClassName('tab-link');
    let tabContents = document.getElementsByClassName('tab-content');

    // Nasconde tutti i contenuti dei tab e rimuove la classe 'active'
    for (let i = 0; i < tabLinks.length; i++) {
        tabLinks[i].className = tabLinks[i].className.replace('active', "");
        tabContents[i].style.display = 'none';
    }

    // Mostra il contenuto del tab selezionato e aggiunge la classe 'active'
    document.getElementById(tabName).style.display = 'block';
    evt.currentTarget.className += " active";
}

function copyToClipboard(btn) {
    // Naviga al genitore comune, quindi seleziona il div che contiene il testo
    let textContainer = btn.parentElement.querySelector('div');
    let text = textContainer.innerText; // Ottiene il testo da copiare

    navigator.clipboard.writeText(text).then(() => {
        btn.textContent = '‚úî Copiato!'; // Cambia temporaneamente il testo del bottone
        setTimeout(() => {
            btn.textContent = 'üìã Copia testo'; // Ripristina il testo originale del bottone dopo 3 secondi
        }, 3000);
    }).catch(err => {
        console.error('Errore durante la copia: ', err);
    });
}

function changeLanguage(lang) {
    let tabContents = document.getElementsByClassName('tab-content');

    // Itera su tutti i tab e cambia il testo in base alla lingua selezionata
    for (let i = 0; i < tabContents.length; i++) {
        let tabId = tabContents[i].id; // L'id del tab corrisponde alla chiave in `descriptions`
        let textContainer = tabContents[i].querySelector('div');
        // Assicurati che la traduzione per la lingua selezionata esista prima di tentare di sostituirla
        if (descriptions[tabId][lang]) {
            textContainer.innerHTML = descriptions[tabId][lang].replace(/\n/g, '<br>');
        } else {
            console.error('Traduzione non disponibile per la lingua: ' + lang);
        }
    }
}

